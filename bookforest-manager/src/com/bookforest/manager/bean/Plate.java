package com.bookforest.manager.bean;

public class Plate {

	private Integer plateId;
	
	private String plateName;
	
	private Integer articleNum;
	
	private Integer plateView;
	
	private String plateImg;
	
	
	private Integer plateFollow;
	
	private String plateInfo;
	
	private String platePost;
	
	private Integer plateState;
	
	private String operatorId;
	
	private String operatorTime;

	public Integer getPlateId() {
		return plateId;
	}

	public void setPlateId(Integer plateId) {
		this.plateId = plateId;
	}

	public String getPlateName() {
		return plateName;
	}

	public void setPlateName(String plateName) {
		this.plateName = plateName;
	}

	public Integer getArticleNum() {
		return articleNum;
	}

	public void setArticleNum(Integer articleNum) {
		this.articleNum = articleNum;
	}

	public Integer getPlateView() {
		return plateView;
	}

	public void setPlateView(Integer plateView) {
		this.plateView = plateView;
	}

	public String getPlateImg() {
		return plateImg;
	}

	public void setPlateImg(String plateImg) {
		this.plateImg = plateImg;
	}

	public Integer getPlateFollow() {
		return plateFollow;
	}

	public void setPlateFollow(Integer plateFollow) {
		this.plateFollow = plateFollow;
	}

	public String getPlateInfo() {
		return plateInfo;
	}

	public void setPlateInfo(String plateInfo) {
		this.plateInfo = plateInfo;
	}

	public String getPlatePost() {
		return platePost;
	}

	public void setPlatePost(String platePost) {
		this.platePost = platePost;
	}


	public Integer getPlateState() {
		return plateState;
	}

	public void setPlateState(Integer plateState) {
		this.plateState = plateState;
	}

	public String getOperatorId() {
		return operatorId;
	}

	public void setOperatorId(String operatorId) {
		this.operatorId = operatorId;
	}

	public String getOperatorTime() {
		return operatorTime;
	}

	public void setOperatorTime(String operatorTime) {
		this.operatorTime = operatorTime;
	}
	
	
	
} 
